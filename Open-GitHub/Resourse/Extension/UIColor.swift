import UIKit

extension UIColor {
    open class var gitHubBasicColor: UIColor {
        get {
            return UIColor(red: 36/255, green: 41/255, blue: 46/255, alpha: 1)
        }
    }
    
    open class var gitHubLightGrayColor: UIColor {
        get {
            return UIColor(red: 255/255, green: 255/255, blue: 255/255, alpha: 0.175)
        }
    }
    
    open class var gitHubWhiteColor: UIColor {
        get {
            return UIColor(red: 246/255, green: 248/255, blue: 250/255, alpha: 1)
        }
    }
}
